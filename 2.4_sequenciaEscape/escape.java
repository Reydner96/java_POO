public class escape {

  public static void main(String[] args) {
    System.out.println("Isso \"e um escape\"");
    System.out.println("Isso \\tmb e\\ um escape");
    System.out.println("Isso \n tmb");
  }
}

/* Um escape nada mais que utilizar a \ para
 pular ou escapar. Conforme o exemplo a cima
 a contra barra foi usada para pular a aspas dupla
 que estava fechando texto e continuar imprimindo o
 texto ate o final */

