public class precoMedio {

  public static void main(String[] args) {
    int preco1 = 25;
    int preco2 = 22;
    int preco3 = 28;
    int preco4 = 21;

    int mediaPreco = (preco1 + preco2 + preco3 + preco4) / 4;

    System.out.println("\"Sistema de Calculo de Media\"");
    System.out.println("Preço 1: " + preco1);
    System.out.println("Preço 2: " + preco2);
    System.out.println("Preço 3: " + preco3);
    System.out.println("Preço 4: " + preco4);
    System.out.println("\nMedia dos preços:\n" + mediaPreco);
  }

}
